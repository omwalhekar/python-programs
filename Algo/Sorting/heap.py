def insertCondition(heapArr, index):
    if(index % 2 == 0):
        parent = index//2 - 1
    else:
        parent = index//2

    if(heapArr[parent] >= heapArr[index]):
        return True
    else:
        return False


def insert(heapArr, data):
    n = len(heapArr)
    if(n == 0):
        heapArr.append(data)
    else:
        heapArr.append(data)
        index = n
        while(index > 0):
            if(not insertCondition(heapArr, index)):
                if(index % 2 == 0):
                    parent = index//2 - 1
                else:
                    parent = index//2
                heapArr[parent], heapArr[index] = heapArr[index], heapArr[parent]
                index = parent
            else:
                return


def getLeftChild(heapArr, index):
    if(index % 2 == 0):
        lchild = index*2 + 1
    else:
        lchild = index*2 + 2

    return lchild


def getRightChild(heapArr, index):
    if(index % 2 == 0):
        rchild = index*2 + 2
    else:
        rchild = index*2 + 1

    return rchild


def delete(heapArr, cnt):
    n = len(heapArr)
    if(n == 0):
        return
    else:
        if(n == 1):
            return
        else:
            heapArr[-cnt], heapArr[0] = heapArr[0], heapArr[-cnt]
            n -= cnt
        parent = 0
        while(parent < n):
            lchild = getLeftChild(heapArr, parent)
            rchild = getRightChild(heapArr, parent)
            if(rchild > n-1 and lchild > n-1):
                return
            elif(rchild < n and lchild < n):
                if(heapArr[lchild] > heapArr[rchild]):
                    child = lchild
                else:
                    child = rchild
            elif(rchild < n):
                child = rchild
            else:
                child = lchild

            if(heapArr[parent] < heapArr[child]):
                heapArr[parent], heapArr[child] = heapArr[child], heapArr[parent]
            parent = child


def heapify(arr):
    heapArr = []
    cnt = 1
    for i in range(len(arr)):
        insert(heapArr, arr[i])
    for i in range(len(arr)):
        delete(heapArr, cnt)
        cnt += 1
    return heapArr


